#AknurAiganym Пайдаланушыдан температураны Цельсий бойынша енгізуді сұраймыз
celsius = float(input("Цельсий бойынша температурасын енгізіңіз: "))

# Цельсийден Фаренгейтке түрлендіру
fahrenheit = (celsius * 9/5) + 32

# Түрлендірілген температураны экранға шығару
print(f"Фаренгейт бойынша температура: {fahrenheit}°F")

# Температураға байланысты хабарлама шығару
if celsius < 0:
    print("Су мұз күйінде")
elif celsius > 100:
    print("Су қайнап жатыр")
