#AknurAiganym Пайдаланушыдан үш қабырғаны енгізуді сұрау
a = float(input("Бірінші қабырғаны енгізіңіз: "))
b = float(input("Екінші қабырғаны енгізіңіз: "))
c = float(input("Үшінші қабырғаны енгізіңіз: "))

# Үшбұрыштың түрін анықтау
if a == b == c:
    print("Теңқабырғалы үшбұрыш")
elif a == b or b == c or a == c:
    print("Теңбүйірлі үшбұрыш")
else:
    print("Әртүрлі қабырғалы үшбұрыш")
