#AknurAiganym Пайдаланушыдан үш бүтін сан сұрау
num1 = int(input("Бірінші санды енгізіңіз: "))
num2 = int(input("Екінші санды енгізіңіз: "))
num3 = int(input("Үшінші санды енгізіңіз: "))

# Ең үлкен санды анықтау
largest = max(num1, num2, num3)

# Нәтижені шығару
print("Ең үлкен сан:", largest)
