score = int(input("0-ден 100-ге дейінгі балды енгізіңіз: "))

# Бағалау шарттары
if 90 <= score <= 100:
    print("Өте жақсы")
elif 70 <= score <= 89:
    print("Жақсы")
elif 50 <= score <= 69:
    print("Қанағаттанарлық")
elif 0 <= score <= 49:
    print("Қанағаттанарлықсыз")
else:
    print("Қате енгізу! Балл тек 0-ден 100-ге дейін болуы тиіс.")
